int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_minus(int a, int b)
{
	return (a - b);
}

int	ft_devide(int a, int b)
{
	return (a / b);
}

int	ft_multiply(int a, int b)
{
	return (a * b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
