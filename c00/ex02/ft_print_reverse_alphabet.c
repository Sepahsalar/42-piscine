#include <unistd.h>

void	ft_putchar(char s)
{
	write(1, &s, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	c;

	c = 'z';
	while (c >= 'a')
	{
		ft_putchar(c);
		c--;
	}
}
