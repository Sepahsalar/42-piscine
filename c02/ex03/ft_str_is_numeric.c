int	ft_str_is_numeric(char *str)
{
	int	i;
	int	r;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			r = 1;
		else
			return (0);
		i++;
	}
	r = 1;
	return (r);
}
