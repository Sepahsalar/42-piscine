int	ft_str_is_alpha(char *str)
{
	int	i;
	int	r;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z'))
			r = 1;
		else
			return (0);
		i++;
	}
	r = 1;
	return (r);
}
